public class TblModaTambahan {
    
    private String car, moda, carrier, voy, bendera;
    private int seri;

    public String getCar() {
        return this.car;
    }

    public void setCar(String car) {
        this.car = car;
    }

    public String getModa() {
        return this.moda;
    }

    public void setModa(String moda) {
        this.moda = moda;
    }

    public String getCarrier() {
        return this.carrier;
    }

    public void setCarrier(String carrier) {
        this.carrier = carrier;
    }

    public String getVoy() {
        return this.voy;
    }

    public void setVoy(String voy) {
        this.voy = voy;
    }

    public String getBendera() {
        return this.bendera;
    }

    public void setBendera(String bendera) {
        this.bendera = bendera;
    }

    public int getSeri() {
        return this.seri;
    }

    public void setSeri(int seri) {
        this.seri = seri;
    }
}
